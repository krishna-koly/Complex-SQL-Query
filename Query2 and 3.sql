#Question 01: Write a SQL query to fetch the second last record from employee table

#Answer:
with cte as(
select *, row_number() over( order by emp_id desc) as rnk
from employee
)
select emp_ID,emp_NAME,DEPT_NAME,SALARY
FROM cte
where rnk = 2;

#Question 02: Write a SQL query to display only the details of employees who either earn the 
#highest salary or the lowest salary in each department from the employee table.

select * from employee;

WITH t1 AS(
SELECT *,
MAX(salary) OVER(partition BY dept_name) max,
MIN(salary) OVER(partition BY dept_name) min
FROM employee)
SELECT * from t1
WHERE salary IN (max,min)
ORDER BY dept_name








